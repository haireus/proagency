document.addEventListener('DOMContentLoaded', function () {
  // The default language is English
  var lang = localStorage.getItem('language');
  if (!lang || lang == null) {
    var lang = 'en-gb';
  }

  // Function to update language for elements with the class "lang"
  function updateLanguage() {
    var elements = document.querySelectorAll('.lang');
    elements.forEach(function (element) {
      var key = element.getAttribute('key');
      element.innerHTML = arrLang[lang][key];
    });
  }

  // Set default language on page load
  updateLanguage();

  // Event listener for click on elements with the class "translate"
  var translateButtons = document.querySelectorAll('.translate');
  translateButtons.forEach(function (button) {
    button.addEventListener('click', function () {
      localStorage.setItem('language', this.id);
      lang = this.id;
      updateLanguage();
    });
  });
});
